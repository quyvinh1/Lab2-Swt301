public class Star {
    private int id;
    private int star;

    public Star() {
    }

    public Star(int id, int star) {
        this.id = id;
        this.star = star;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getStar() {
        return star;
    }

    public void setStar(int star) {
        this.star = star;
    }
    
    
}
